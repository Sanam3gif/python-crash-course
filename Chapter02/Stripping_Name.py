# Exercise 2-7. Stripping Names: Use a variable to represent a person's name, and include some whitespace character at the beginning and end of the name. 
# Make sure you each character comination, "\t" and "\n", at least once.
# Print the name once, so the whitespace around the name is displayed. 
# Then print the name using each of the three stripping functions, lstrip(), 
# rstrip(), and strip(). 

Name = " \t\nsanan "
print(Name)
print(Name.lstrip())
print(Name.rstrip())
print(Name.strip())

